public class Book {
    // Attributes
    private String name;
    private String author;
    private int pages;

    // Constructor
    public Book(String name, String author, int pages) {
        this.name = name;
        this.author = author;
        this.pages = pages;

    }

    //Getters & setters

    public String getName() {
        return name;
    }
}
